package com.edu.beans;

public class SellerVo {
	private String id;
	private String passwd;
	private int money;
	private int apple_count;
	private int apple_price;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getApple_count() {
		return apple_count;
	}
	public void setApple_count(int apple_count) {
		this.apple_count = apple_count;
	}
	public int getApple_price() {
		return apple_price;
	}
	public void setApple_price(int apple_price) {
		this.apple_price = apple_price;
	}
}
